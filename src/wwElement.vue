<template>
    <div class="tabs-element" :class="content.tabsPosition" :style="cssVariables">
        <div
            v-if="content.fixedToTop && content.tabFields.items"
            ref="fixedTabs"
            class="tabs-container fixedToTop"
            :class="content.tabsPosition"
            :style="cssTabsFixedPosition"
        >
            <div v-for="index in nbOfTabs" :key="index" class="layout-container" @click="changeTab(index)">
                <div class="layout-sublayout">
                    <wwLayout class="layout -layout" :path="`tabsList[${index + 1}]`">
                        <template #default="{ item }">
                            <wwLayoutItem>
                                <wwElement
                                    v-bind="item"
                                    :states="index === currentTabIndex ? ['active'] : []"
                                ></wwElement>
                            </wwLayoutItem>
                        </template>
                    </wwLayout>
                </div>
            </div>
        </div>
        <div v-if="content.tabsList && !content.fixedToTop" class="tabs-container" :class="content.tabsPosition">
            <div v-for="index in nbOfTabs" :key="index" class="layout-container" @click="changeTab(index)">
                <div class="layout-sublayout">
                    <wwLayout class="layout -layout" :path="`tabsList[${index}]`"
                        ><template #default="{ item }">
                            <wwLayoutItem>
                                <wwElement
                                    v-bind="item"
                                    :states="index === currentTabIndex ? ['active'] : []"
                                ></wwElement>
                            </wwLayoutItem> </template
                    ></wwLayout>
                </div>
            </div>
        </div>
        <transition :name="activeTransition" mode="out-in">
            <div :key="currentTabIndex" class="tabs-content">
                <wwLayout
                    class="layout -layout"
                    :class="{ isEditing: isEditing }"
                    :path="`tabsContent[${currentTabIndex}]`"
                >
                </wwLayout>
            </div>
        </transition>
    </div>
</template>

<script>
export default {
    props: {
        content: { type: Object, required: true },
        /* wwEditor:start */
        wwEditorState: { type: Object, required: true },
        /* wwEditor:end */
    },
    emits: ['update:content'],
    data() {
        return {
            currentTabIndex: 1,
            activeTransition: 'fade',
        };
    },
    computed: {
        isEditing() {
            /* wwEditor:start */
            return this.wwEditorState.editMode === wwLib.wwEditorHelper.EDIT_MODES.EDITION;
            /* wwEditor:end */
            // eslint-disable-next-line no-unreachable
            return false;
        },
        nbOfTabs() {
            return this.content.tabsList.length;
        },
        cssVariables() {
            return {
                '--tab-transition-duration': this.content.transitionDuration + 's',
            };
        },
        cssTabsFixedPosition() {
            return {
                '--tab-leftRight-position': this.content.leftRightPosition,
                '--tab-topBottom-position': this.content.topBottomPosition,
            };
        },
        getSublayoutHeight() {
            const elHeight = document.querySelectorAll('.tabs-sublayout-container');
            if (elHeight && elHeight.length && elHeight[this.currentTabIndex]) {
                return `${elHeight[this.currentTabIndex].offsetHeight}px`;
            }

            return '24px';
        },
    },
    watch: {
        'wwEditorState.sidepanelContent.tabIndex'(newIndex) {
            this.currentTabIndex = newIndex;
        },
    },
    methods: {
        changeTab(index) {
            if (index < 0) return;
            this.order = index > this.currentTabIndex ? 'after' : 'before';
            this.handleTransition(this.order);
            this.currentTabIndex = index;
        },
        /* wwEditor:start */
        async addTab() {
            const tabsList = [...this.content.tabsList];
            const subTabLayouts = [...this.content.subTabLayouts];
            const tabsContent = [...this.content.tabsContent];
            if (tabsList.length === 0) {
                tabsList.push([]);
                subTabLayouts.push([]);
                tabsContent.push([]);
            } else {
                const tab = tabsList[tabsList.length - 1];
                const subTab = subTabLayouts[subTabLayouts.length - 1];
                const content = tabsContent[tabsContent.length - 1];
                tabsList.push(tab);
                subTabLayouts.push(subTab);
                tabsContent.push(content);
            }
            this.$emit('update:content', { tabsList, subTabLayouts, tabsContent });
        },
        removeTab(index) {
            const tabsList = [...this.content.tabsList];
            const subTabLayouts = [...this.content.subTabLayouts];
            const tabsContent = [...this.content.tabsContent];
            tabsList.splice(index, 1);
            subTabLayouts.splice(index, 1);
            tabsContent.splice(index, 1);
            this.$emit('update:content', { tabsList, subTabLayouts, tabsContent });
        },
        /* wwEditor:end */
        handleTransition(order) {
            switch (this.content.transition) {
                case 'fade':
                    this.activeTransition = 'fade';
                    break;
                case 'none':
                    this.activeTransition = '';
                    break;
                case 'fadeTopBottom':
                    this.activeTransition = order === 'after' ? 'fadeTop' : 'fadeBottom';
                    break;
                case 'fadeLeftRight':
                    this.activeTransition = order === 'after' ? 'fadeLeft' : 'fadeRight';
                    break;
                default:
                    this.activeTransition = 'fade';
            }
        },
    },
};
</script>

